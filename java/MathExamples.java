public class MathExamples {
    public static void main(String[] args) {
        System.out.println("Math.abs(-5) = " + Math.abs(-5));
        System.out.println("Math.ceil(3/2) = " + Math.ceil(3 / 2)); // Integer division 3/2 = 1
        System.out.println("Math.ceil((double)3/2) = " + Math.ceil((double) 3 / 2)); // Floating point division 3/2 =
                                                                                     // 1.5
        System.out.println("Math.ceil(4.5) = " + Math.ceil(4.5));
        System.out.println("Math.floor(4.5) = " + Math.floor(4.5));
        System.out.println("Math.max(4, 5) = " + Math.max(4, 5));
        System.out.println("Math.min(4, 5) = " + Math.min(4, 5));
        System.out.println("Math.pow(2, 3) = " + Math.pow(2, 3));
        System.out.println("Math.sqrt(16) = " + Math.sqrt(16));
    }
}
